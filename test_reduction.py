import reduction as t

def test_reduction():
    # TEST 1 Paper Eg1
    firms_prefs = {
                        1: [[1,2],[1,5],[2,5],[1,3],[4,5],[2,4],[1,4],[3,4],[3,5],[2,3],[1],[4],[3],[2],[5]],
                        2: [[3,6],[3,5],[5,6],[2,5],[1,3],[2,6],[1,5],[1,2],[2,3],[1,6],[1],[2],[3],[5],[6]],
                        3: [[2,4],[1,2],[3,4],[2,3],[1,3],[1,4],[1],[2],[3],[4]]
              }

    workers_prefs = {
                            1: [[3],[1],[2]],
                            2: [[2,3],[1,3],[1,2],[1],[2],[3]],
                            3: [[1],[2]],
                            4: [[1],[3],[2]],
                            5: [[2],[3]],
                            6: [[1,3],[3],[1]]
                    }
    assert t.reduction(firms_prefs, workers_prefs) == [
        {
            1: [[1, 2], [1, 3], [2, 4], [1, 4], [3, 4], [2, 3], [1], [4], [3], [2]],
            2: [[3, 5], [2, 5], [2, 3], [2], [3], [5]],
            3: [[2, 4], [1, 2], [1, 4], [1], [2], [4]]},
        {
            1: [[3], [1]],
            2: [[2, 3], [1, 3], [1, 2], [1], [2], [3]],
            3: [[1], [2]],
            4: [[1], [3]],
            5: [[2]],
            6: []}
    ]

    # TEST 2 - Paper Eg2
    firms_prefs = {
                            1: [[2],[1],[3],[4]],
                            2: [[4],[2],[3],[1]],
                            3: [[4],[2],[3],[1]],
                            4: [[3],[1],[4],[2]]
                }

    workers_prefs = {
                            1: [[2],[1],[4],[3]],
                            2: [[4],[3],[2],[1]],
                            3: [[3],[1],[4],[2]],
                            4: [[1],[3],[4],[2]]
                    }
    assert t.reduction(firms_prefs, workers_prefs, one_to_one=True) == [{1: [[1], [3], [4]], 2: [[2], [1]], 3: [[4], [2], [3]], 4: [[3], [2]]},
                                                                        {1: [[2], [1]], 2: [[4], [3], [2]], 3: [[3], [1], [4]], 4: [[1], [3]]}]